if (!function_exists('checkUuid')) {
    function checkUuid($uuid) {
        if (preg_match('/^[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[1-5][0-9a-fA-F]{3}-[89abAB][0-9a-fA-F]{3}-[0-9a-fA-F]{12}$/', $uuid)) {
            return $uuid;
        } else {
            return  abort(404);
        }
    }
}
