if (!function_exists('getHalkatId')) {
    function getHalkatId($fromGrade, $toGrade) {
        $halkat = null;
        $halkat_basic = null;

        if ($fromGrade == 1 && $toGrade == 4) {
            $halkat = 1;
            $halkat_basic = 1;
        } elseif ($fromGrade >= 1 && $toGrade <= 4) {
            $halkat = 6;
            $halkat_basic = 1;
        } elseif ($fromGrade == 5 && in_array($toGrade, [9, 10])) {
            $halkat = 2;
            $halkat_basic = 2;
        } elseif ($fromGrade >= 5 && $toGrade <= 10) {
            $halkat = 7;
            $halkat_basic = 2;
        } elseif ($fromGrade == 10 && $toGrade == 12) {
            $halkat = 3;
            $halkat_basic = 3;
        } elseif ($fromGrade == 11 && $toGrade == 12) {
            $halkat = 4;
            $halkat_basic = 4;
        } elseif ($fromGrade == 1 && in_array($toGrade, [9, 10, 12])) {
            $halkat = 5;
            $halkat_basic = 5;
        } elseif ($fromGrade == 5 && $toGrade == 12) {
            $halkat = 5;
            $halkat_basic = 5;
        } elseif ($fromGrade == 1 && $toGrade >= 5) {
            $halkat = 8;
            $halkat_basic = 5;
        } elseif ($fromGrade >= 4 && $toGrade >= 10) {
            $halkat = 8;
            $halkat_basic = 5;
        }
        return [
            'halkat' => $halkat,
            'halkat_basic' => $halkat_basic,
        ];
    }
}
