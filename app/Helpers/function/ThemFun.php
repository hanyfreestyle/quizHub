if (!function_exists('getDarkMode')) {
    function getDarkMode($getVal) {
        if ($getVal == '1') {
            $config = ' darkMode ';
        } else {
            $config = '';
        }
        return $config;
    }
}
if (!function_exists('getDeskView')) {
    function getDeskView($getVal) {
        if ($getVal == 'grid') {
            $config = 'deskGrid';
        } else {
            $config = 'deskList';
        }
        return $config;
    }
}

if (!function_exists('getMobileView')) {
    function getMobileView($getVal) {
        if ($getVal == 'grid') {
            $config = 'mobileGrid';
        } else {
            $config = 'mobileList';
        }
        return $config;
    }
}

if (!function_exists('getIconRadius')) {
    function getIconRadius($getVal) {
        if ($getVal == '1') {
            $config = '50%';
        } else {
            $config = '9px';
        }
        return $config;
    }
}
if (!function_exists('getIconBorder')) {
    function getIconBorder($getVal) {
        if ($getVal == '1') {
            $config = '';
        } else {
            $config = 'listRowNoBorder';
        }
        return $config;
    }
}


if (!function_exists('getIconColor')) {
    function getIconColor($getVal, $data) {
        if ($getVal == '2') {
            $config = $data->input_info->name_key;
        } else {
            $config = '';
        }
        return $config;
    }
}

if (!function_exists('getIconName')) {
    function getIconName($getVal) {
        if ($getVal == '1') {
            $config = true;
        } else {
            $config = false;
        }
        return $config;
    }
}


